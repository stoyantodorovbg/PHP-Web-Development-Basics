<?php


namespace TaskManagement\Data;


class CategoryDTO
{
    private $id;
    private $name;
    private $count;

    public function create($id, $name, $count = null)
    {
        $categoryDTO = new CategoryDTO();

        $categoryDTO->setId($id)->setName($name)->setCount($count);

        return $categoryDTO;
    }

    /**
     * @return mixed
     */
    public function getCount()
    {
        return $this->count;
    }

    /**
     * @param mixed $count
     */
    public function setCount($count)
    {
        $this->count = $count;
        return $this;
    }


    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param mixed $id
     */
    public function setId($id)
    {
        $this->id = $id;
        return $this;
    }

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param mixed $name
     */
    public function setName($name)
    {
        $this->name = $name;
        return $this;
    }


}